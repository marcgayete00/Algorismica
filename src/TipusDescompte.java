public class TipusDescompte {
    private int caixa;
    private int sabata;
    private int tipusDescompte;

    public TipusDescompte(int caixa, int sabata, int tipusDescompte) {
        this.caixa = caixa;
        this.sabata = sabata;
        this.tipusDescompte = tipusDescompte;
    }

    public int getCaixa() {
        return caixa;
    }

    public int getSabata() {
        return sabata;
    }

    public int getTipusDescompte() {
        return tipusDescompte;
    }


}
